    //Locking orientation on some viewcontrollers
    lazy var orientationLock = UIInterfaceOrientationMask.portrait
    
    func application(_ application: UIApplication, supportedInterfaceOrientationsFor window: UIWindow?) -> UIInterfaceOrientationMask {
        return self.orientationLock
    }
    
    //Special function that will lock orientation
    struct AppUtility {
        static func lockOrientation(_ orientation: UIInterfaceOrientationMask) {
            if let delegate = UIApplication.shared.delegate as? AppDelegate {
                delegate.orientationLock = orientation
            }
        }
        
        static func lockOrientation(_ orientation: UIInterfaceOrientationMask, andRotateTo rotateOrientation:UIInterfaceOrientation) {
            self.lockOrientation(orientation)
            UIDevice.current.setValue(rotateOrientation.rawValue, forKey: "orientation")
        }
    }
    
    func beginBackgroundTask(){
        let taskName = "backgroundNotify"
        backgroundTask = UIApplication.shared.beginBackgroundTask(withName: taskName)
    }
    
    func endBackgroundTask(){
        UIApplication.shared.applicationIconBadgeNumber = 0
        ChatViewController.messageBadge = 0
        if let task = backgroundTask{
            UIApplication.shared.endBackgroundTask(task)
        }
        backgroundTask = UIBackgroundTaskInvalid
    }


}

